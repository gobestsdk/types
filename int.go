package types

const INT_MAX = int(^uint(0) >> 1)
const INT_MIN = ^INT_MAX

//UniqueInt
func UniqueInts(list []int) (x []int) {
	x = []int{}
	for _, i := range list {
		if len(x) == 0 {
			x = append(x, i)
		} else {
			for k, v := range x {
				if i == v {
					break
				}
				if k == len(x)-1 {
					x = append(x, i)
				}
			}
		}
	}
	return x
}

